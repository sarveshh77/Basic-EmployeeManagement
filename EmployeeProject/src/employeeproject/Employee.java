    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        
        
        
           getInfo();
           if(id.equals(""))
           {
               JOptionPane.showMessageDialog(null, "Please Enter id to update employee", "Error", JOptionPane.ERROR_MESSAGE);
           }


String updateEmployeeQuery = "UPDATE employee_table SET name = ?, gender = ?, emaill = ? WHERE id = ?";
String updateDepartmentQuery = "UPDATE department SET DepartmentName = ? WHERE ManagerID = ?";
String updateAddressQuery = "UPDATE address_table SET address = ? WHERE address_id = (SELECT address_id FROM employee_table WHERE id = ?)";
String updateRoleQuery = "UPDATE role SET role_name = ? WHERE role_id = (SELECT role_id FROM employee_table WHERE id = ?)";
if (id != null && !id.isEmpty()) {
try {
    
    if (name != null && !name.isEmpty() || gender != null && !gender.isEmpty() || email != null && !email.isEmpty()) 
    {
        PreparedStatement pstmtEmployee = con.prepareStatement(updateEmployeeQuery);
        
        
        if (name != null && !name.isEmpty()) {
            pstmtEmployee.setString(1, name);
        } else {
            pstmtEmployee.setNull(1, java.sql.Types.VARCHAR);
        }
        
       
        if (gender != null && !gender.isEmpty()) {
            pstmtEmployee.setString(2, gender);
        } else {
            pstmtEmployee.setNull(2, java.sql.Types.VARCHAR);
        }

        if (email != null && !email.isEmpty()) {
            pstmtEmployee.setString(3, email);
        } else {
            pstmtEmployee.setNull(3, java.sql.Types.VARCHAR);
        }

        pstmtEmployee.setString(4, id);
        pstmtEmployee.executeUpdate();
    }

  
    if (dept != null && !dept.isEmpty()) {
        PreparedStatement pstmtDepartment = con.prepareStatement(updateDepartmentQuery);
        pstmtDepartment.setString(1, dept);
        pstmtDepartment.setString(2, id);  
        pstmtDepartment.executeUpdate();
    }

   
    if (address != null && !address.isEmpty()) {
        PreparedStatement pstmtAddress = con.prepareStatement(updateAddressQuery);
        pstmtAddress.setString(1, address); 
        pstmtAddress.setString(2, id);       
        pstmtAddress.executeUpdate();
    }

   
    if (role != null && !role.isEmpty()) {
        PreparedStatement pstmtRole = con.prepareStatement(updateRoleQuery);
        pstmtRole.setString(1, role);  
        pstmtRole.setString(2, id);  
        pstmtRole.executeUpdate();
    }

    JOptionPane.showMessageDialog(null, "Employee with ID " + id + " updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
} catch (Exception e) {
    System.out.print(e);
}

} 
        
    }//GEN-LAST:event_jButton2ActionPerformed
  
