    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       getInfo();
model = (DefaultTableModel) jTable2.getModel();
model.setRowCount(0);

if (id.equals("")) {
    JOptionPane.showMessageDialog(null, "Please Enter id to search employee", "Error", JOptionPane.ERROR_MESSAGE);
} else {
    try {
        
        String query = "SELECT e.id, e.name, d.DepartmentName, e.gender, e.emaill, a.address FROM employee_table e JOIN department d ON e.id = d.ManagerID  JOIN address_table a ON e.address_id = a.address_id WHERE e.id = ?";  // Use parameterized query

        
        PreparedStatement pstmt = con.prepareStatement(query);
        pstmt.setString(1, id);  

        
        ResultSet rs = pstmt.executeQuery();          
       
        if (rs.next()) {
            String employeeId = rs.getString("id");
            String name = rs.getString("name");
            String department = rs.getString("DepartmentName");
            String gender = rs.getString("gender");
            String email = rs.getString("emaill");  
            String address = rs.getString("address");

            
            model.addRow(new Object[]{employeeId, name, department, email, gender, address});
        } else {
            
            JOptionPane.showMessageDialog(null, "No employee found with ID: " + id, "Not Found", JOptionPane.ERROR_MESSAGE);
        }

    } catch (SQLException ex) {
      
        Logger.getLogger(Employee.class.getName()).log(Level.SEVERE, null, ex);
    }
}


    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        getInfo();
        if (!id.equals("")) {

            try 
            {
            String deleteAddressQuery = "DELETE FROM address_table WHERE address_id = ?";
            PreparedStatement addressStmt = con.prepareStatement(deleteAddressQuery);
            addressStmt.setString(1, id);
            addressStmt.executeUpdate();
            
            String deleteRoleQuery = "DELETE FROM role   WHERE e_id = ?";
            PreparedStatement roleStmt = con.prepareStatement(deleteRoleQuery);
            roleStmt.setString(1, id);
            roleStmt.executeUpdate();
                    
            String deleteDepartmentQuery = "DELETE FROM department WHERE ManagerID = ?";
            PreparedStatement departmentStmt = con.prepareStatement(deleteDepartmentQuery);
            departmentStmt.setString(1, id);
            departmentStmt.executeUpdate();
            
            String deleteEmployeeQuery = "DELETE FROM employee_table WHERE id = ?";
            PreparedStatement employeeStmt = con.prepareStatement(deleteEmployeeQuery);
            employeeStmt.setString(1, id);
            int rowsAffected = employeeStmt.executeUpdate();
           
            
            if (rowsAffected > 0) {
                
                JOptionPane.showMessageDialog(null, "Employee with ID " + id + " deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
            else 
            {
            
                JOptionPane.showMessageDialog(null, "No employee found with ID: " + id, "Not Found", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            
            System.out.print(e);
            JOptionPane.showMessageDialog(null, "Error occurred while deleting the employee.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } 
   else 
   {
        JOptionPane.showMessageDialog(null, "Employee ID cannot be empty.", "Input Error", JOptionPane.WARNING_MESSAGE);
    }      
    }//GEN-LAST:event_jButton7ActionPerformed

